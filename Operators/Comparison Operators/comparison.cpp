#include <iostream>
using namespace std;

int main()
{
    int number1 = 12;
    int number2 = 8;

    cout << (number1 == number2) << endl;
    cout << (number1 != number2) << endl;
    cout << (number1 < number2) << endl;
    cout << (number1 > number2) << endl;
    cout << (number1 <= number2) << endl;
    cout << (number1 >= number2) << endl;
}